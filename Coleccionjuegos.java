package com.sample;


public class Coleccionjuegos {

  private long id;
  private String nombre;
  private long anio;
  private long numJugadores;
  private String categoria;
  private String plataforma;
  private String estudio;
  private String empresa;
  private String formato;
  private String estadoJuego;
  private String estadoCaja;
  private long usuarioId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }


  public long getAnio() {
    return anio;
  }

  public void setAnio(long anio) {
    this.anio = anio;
  }


  public long getNumJugadores() {
    return numJugadores;
  }

  public void setNumJugadores(long numJugadores) {
    this.numJugadores = numJugadores;
  }


  public String getCategoria() {
    return categoria;
  }

  public void setCategoria(String categoria) {
    this.categoria = categoria;
  }


  public String getPlataforma() {
    return plataforma;
  }

  public void setPlataforma(String plataforma) {
    this.plataforma = plataforma;
  }


  public String getEstudio() {
    return estudio;
  }

  public void setEstudio(String estudio) {
    this.estudio = estudio;
  }


  public String getEmpresa() {
    return empresa;
  }

  public void setEmpresa(String empresa) {
    this.empresa = empresa;
  }


  public String getFormato() {
    return formato;
  }

  public void setFormato(String formato) {
    this.formato = formato;
  }


  public String getEstadoJuego() {
    return estadoJuego;
  }

  public void setEstadoJuego(String estadoJuego) {
    this.estadoJuego = estadoJuego;
  }


  public String getEstadoCaja() {
    return estadoCaja;
  }

  public void setEstadoCaja(String estadoCaja) {
    this.estadoCaja = estadoCaja;
  }


  public long getUsuarioId() {
    return usuarioId;
  }

  public void setUsuarioId(long usuarioId) {
    this.usuarioId = usuarioId;
  }

}
